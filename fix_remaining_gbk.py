def fix_file(filepath):
    """修复单个文件"""
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    original = content
    fix_count = 0
    
    # 应用所有替换规则
    for garbage, correct in GARBAGE_PATTERNS.items():
        if garbage in content:
            content = content.replace(garbage, correct)
            fix_count += 1
    
    if content != original:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
        print(f"✅ 已修复: {filepath} ({fix_count} 处)")
        return fix_count
    else:
        print(f"⏭️  跳过: {filepath} (无需修复)")
        return 0

if __name__ == '__main__':
    import sys
    
    if len(sys.argv) < 2:
        # 默认修复 util.py
        files = ['biliup/common/util.py']
    else:
        files = sys.argv[1:]
    
    total_fixes = 0
    for filepath in files:
        total_fixes += fix_file(filepath)
    
    print(f"\n总计修复: {total_fixes} 处")
