def fix_file(filepath):
    """修复单个文件"""
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()
    except Exception as e:
        print(f"❌ 无法读取: {filepath} - {e}")
        return 0
    
    original = content
    fix_count = 0
    
    # 应用所有替换规则
    for garbage, correct in GARBAGE_PATTERNS.items():
        if garbage in content:
            content = content.replace(garbage, correct)
            fix_count += 1
    
    if content != original:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
        print(f"✅ 已修复: {filepath} ({fix_count} 处)")
        return fix_count
    else:
        return 0

